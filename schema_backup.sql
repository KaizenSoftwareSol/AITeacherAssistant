CREATE FUNCTION public.search_lecture_chunks(p_lecture_id uuid, p_query_embedding public.vector, p_limit integer DEFAULT 5) RETURNS TABLE(chunk_id uuid, chunk_content text, chunk_index integer, chunk_metadata text, similarity_score double precision)
    LANGUAGE plpgsql
    AS $$
BEGIN
  RETURN QUERY
  SELECT 
    lc.id AS chunk_id,
    lc.content AS chunk_content,
    lc.chunk_index AS chunk_index,
    lc.chunk_metadata AS chunk_metadata,
    1 - (le.embedding <=> p_query_embedding) AS similarity_score
  FROM public.lecture_embedding le
  JOIN public.lecture_chunk lc ON le.chunk_id = lc.id
  WHERE le.lecture_id = p_lecture_id
  ORDER BY le.embedding <=> p_query_embedding
  LIMIT p_limit;
END;
$$;


