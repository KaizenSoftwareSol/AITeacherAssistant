CREATE VIEW public.student_course_lectures AS
 SELECT e.student_id,
    e.course_id,
    c.code AS course_code,
    c.name AS course_name,
    l.id AS lecture_id,
    l.title AS lecture_title,
    l.description AS lecture_description,
    l.summary AS lecture_summary,
    l.status AS lecture_status,
    l.has_embeddings,
    l.created_at AS lecture_created_at,
    t.id AS teacher_id,
    (((u.first_name)::text || ' '::text) || (u.last_name)::text) AS teacher_name
   FROM ((((public.enrollment e
     JOIN public.course c ON ((e.course_id = c.id)))
     JOIN public.lecture l ON ((l.course_id = c.id)))
     JOIN public.teacher t ON ((l.teacher_id = t.id)))
     JOIN public.users u ON ((t.user_id = u.id)))
  WHERE ((e.is_active = true) AND ((l.status)::text = ANY ((ARRAY['PUBLISHED'::character varying, 'DELIVERED'::character varying])::text[])));


